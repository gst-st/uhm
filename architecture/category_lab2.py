def pillar_E():
    hdr("[E] THE SELF IS A CANONICAL RETRACTION — Ψ idempotent (Ψ∘Ψ=Ψ)")
    rng = np.random.default_rng(5)
    a = rng.standard_normal((N, N)) + 1j * rng.standard_normal((N, N))
    struct = herm(a); struct = struct / np.linalg.norm(struct)

    def phi(rho):
        out = 0.5 * rho + 0.5 * (struct @ rho @ struct.conj().T)
        out = project_psd(herm(out))
        t = np.trace(out).real
        return out / t if t > 1e-12 else np.eye(N) / N

    def psi(rho):                        # run self-observation to convergence
        for _ in range(5000):
            r2 = phi(rho)
            if np.linalg.norm(r2 - rho) < 1e-14:
                return r2
            rho = r2
        return rho

    worst = 0.0
    for _ in range(6):
        a = rng.standard_normal((N, N)) + 1j * rng.standard_normal((N, N))
        r = project_psd(herm(a @ a.conj().T)); r = r / np.trace(r).real
        s1 = psi(r)
        s2 = psi(s1)                     # apply the converged self-model again
        worst = max(worst, np.linalg.norm(s2 - s1))
    print(f"  max ‖Ψ(Ψρ) − Ψρ‖ over random states = {worst:.2e}")
    ok = worst < 1e-9
    print(f"  ⇒ Ψ is idempotent: a retraction onto the self-representable")
    print(f"    states. The 'I' is a projection (a splitting idempotent, so a")
    print(f"    genuine subobject by Karoubi). {ok} [Т]")
