def perm_matrix(perm):
    """7x7 permutation matrix for perm (perm[p-1] = image of point p)."""
    U = np.zeros((N, N))
    for p in range(1, 8):
        U[perm[p - 1] - 1, p - 1] = 1.0
    return U


def encode2(acts):
    """acts = list of (kw, line, mu). Each activation lands on its gate's
    canonical object (classify): voice→population, pair→that coherence,
    line/triangle→the triple's three pairs at 1/3, source→grey. Point-indexed."""
    w = np.zeros(N)
    G = np.zeros((N, N), complex)
    for (kw, line, mu) in acts:
        kind, obj = classify(kw)
        idx = sorted(p - 1 for p in obj)      # 0-based point indices
        th = np.pi * line / 3.0
        if kind == "voice":
            w[idx[0]] += mu
        elif kind == "pair":
            i, j = idx
            G[i, j] += BETA * mu * np.exp(1j * th)
            G[j, i] = np.conj(G[i, j])
            w[i] += mu / 2
            w[j] += mu / 2
        elif kind in ("line", "triangle"):
            for a in range(3):
                for b in range(a + 1, 3):
                    i, j = idx[a], idx[b]
                    G[i, j] += BETA * mu / 3 * np.exp(1j * th)
                    G[j, i] = np.conj(G[i, j])
            for i in idx:
                w[i] += mu / 3
        # source: contributes to grey only
    ws = w.sum()
    for i in range(N):
        G[i, i] += (1 - ALPHA) / N + (ALPHA * w[i] / ws if ws > 0 else 0.0)
    G = project_psd(herm(G))
    return G / np.trace(G).real


def act_on_gate(perm, kw):
    """g·gate: the gate whose support is g·(support of kw)."""
    sup = frozenset(perm[p - 1] for p in support(kw))
    # find the King-Wen gate with this support
    for k in range(1, 65):
        if frozenset(support(k)) == sup:
            return k
    raise AssertionError("no gate for permuted support")


def encode_raw(acts):
    """The raw prior BEFORE project_psd: populations on the diagonal, complex
    coherences on the off-diagonal. Isolates the magnitude from the phase."""
    w = np.zeros(N)
    G = np.zeros((N, N), complex)
    for (kw, line, mu) in acts:
        kind, obj = classify(kw)
        idx = sorted(p - 1 for p in obj)
        th = np.pi * line / 3.0
        if kind == "voice":
            w[idx[0]] += mu
        elif kind == "pair":
            i, j = idx
            G[i, j] += BETA * mu * np.exp(1j * th)
            G[j, i] = np.conj(G[i, j])
            w[i] += mu / 2; w[j] += mu / 2
        elif kind in ("line", "triangle"):
            for a in range(3):
                for b in range(a + 1, 3):
                    i, j = idx[a], idx[b]
                    G[i, j] += BETA * mu / 3 * np.exp(1j * th)
                    G[j, i] = np.conj(G[i, j])
            for i in idx:
                w[i] += mu / 3
    for i in range(N):
        G[i, i] += w[i]
    return G


