import itertools
from fractions import Fraction

import numpy as np
from scipy.linalg import expm

from symmetry_lab import gl32
from prime_radiant import herm, project_psd, purity, H_EFF

N = 7
I7 = np.eye(N) / N


def hdr(t):
    print("=" * 74); print(t); print("=" * 74)


def rand_state(rng):
    a = rng.standard_normal((N, N)) + 1j * rng.standard_normal((N, N))
    g = project_psd(herm(a @ a.conj().T))
    return g / np.trace(g).real


def centered(g):
    return g - I7


def perm_matrix(perm):
    U = np.zeros((N, N))
    for p in range(1, 8):
        U[perm[p - 1] - 1, p - 1] = 1.0
    return U


# ---------------------------------------------------------------------------
# [G] the gathering operad on centered states (barycentric)
# ---------------------------------------------------------------------------

def gather_dir(dirs, weights=None):
    """γ on centered directions: the (weighted) mean commitment."""
    n = len(dirs)
    if weights is None:
        weights = [1.0 / n] * n
    out = np.zeros((N, N), complex)
    for d, w in zip(dirs, weights):
        out = out + w * d
    return out


def meta_state(dirs, weights=None):
    """the physical meta-holon: grey + gathered direction, PSD-projected."""
    g = project_psd(herm(I7 + gather_dir(dirs, weights)))
    return g / np.trace(g).real


def pillar_G():
    hdr("[G] GATHERING IS A SYMMETRIC OPERAD (barycentric, on commitments)")
    rng = np.random.default_rng(3)
    members = [rand_state(rng) for _ in range(5)]
    dirs = [centered(m) for m in members]

    # UNIT: γ_1(D_a) = D_a; and meta([a]) = a
    unit_err = np.linalg.norm(gather_dir([dirs[0]]) - dirs[0])
    meta1_err = np.linalg.norm(meta_state([dirs[0]]) - members[0])
    print(f"  unit  γ_1 = id:                 err = {unit_err:.1e}, "
          f"meta([a]) = a err = {meta1_err:.1e}")

    # SYMMETRY: γ_n is Σ_n-invariant (member order irrelevant)
    worst_sym = 0.0
    for perm in itertools.permutations(range(5)):
        g = gather_dir([dirs[i] for i in perm])
        worst_sym = max(worst_sym, np.linalg.norm(g - gather_dir(dirs)))
    print(f"  symmetry (all 120 orders):     max err = {worst_sym:.1e}")

    # ASSOCIATIVITY with tracked weights: gathering {a,b},{c,d,e} as two units,
    # weighting each member by 1/5, equals the flat γ_5.
    ab = gather_dir(dirs[0:2])                       # unit 1 (2 members)
    cde = gather_dir(dirs[2:5])                      # unit 2 (3 members)
    nested = gather_dir([ab, cde], weights=[2 / 5, 3 / 5])  # weight by size
    flat = gather_dir(dirs)                          # γ_5, each member 1/5
    assoc_err = np.linalg.norm(nested - flat)
    print(f"  associativity (weighted nesting = flat): err = {assoc_err:.1e}")
    print(f"    ⇒ nesting groups-of-groups = gathering everyone (barycenter)")

    # the physical PSD projection is the measured correction to strictness
    meta_nested = meta_state([ab, cde], weights=[2 / 5, 3 / 5])
    meta_flat = meta_state(dirs)
    psd_gap = np.linalg.norm(meta_nested - meta_flat)
    print(f"  physical meta-holon PSD correction: {psd_gap:.1e} "
          f"(strict on commitments; small on states)")

    # DEPTH BOUND: the operad's meaningful arity-depth ends at 3 (SAD_MAX)
    lad = {n: Fraction(2, 7) * Fraction(3 ** (n - 1), n + 1)
           for n in range(1, 5)}
    sad = max(n for n, v in lad.items() if v <= 1)
    print(f"  depth bound P_crit^(n): " +
          ", ".join(f"n{n}={v}" for n, v in lad.items()) +
          f" ⇒ SAD_MAX={sad}")
    ok = (unit_err < 1e-9 and meta1_err < 1e-9 and worst_sym < 1e-9
          and assoc_err < 1e-9 and sad == 3)
    print(f"  ⇒ gathering is a symmetric, unital, (weight-)associative operad")
    print(f"    of finite depth 3: group formation is coherently composable."
          f" {ok} [Т]")
    return ok


# ---------------------------------------------------------------------------
# [H] the dynamics as a functor from the time-monoid + individuality obstruction
# ---------------------------------------------------------------------------

def T(t):
    """the one-parameter group generated by H_EFF (unitary conjugation)."""
    U = expm(-1j * H_EFF * t)
    return U


def pillar_H():
    hdr("[H] TIME IS A FUNCTOR FROM (ℝ,+); INDIVIDUALITY IS ITS OBSTRUCTION")
    # SEMIGROUP / FUNCTORIALITY: T_{s+t} = T_s · T_t
    s, t = 0.37, 0.51
    functor_err = np.linalg.norm(T(s + t) - T(s) @ T(t))
    print(f"  functor law T(s+t) = T(s)·T(t):  err = {functor_err:.1e}  "
          f"(a monoid homomorphism (ℝ,+) → U(7))")

    # OBSTRUCTION: [T_t, R_g] fails for all g≠id, and is generated by the
    # infinitesimal commutator [H_EFF, U_g] (d/dt at 0 of the obstruction).
    G = gl32()
    dt = 0.05
    gen_nonzero = 0
    obstruction_nonzero = 0
    for perm in G:
        U = perm_matrix(perm).astype(complex)
        gen = np.linalg.norm(H_EFF @ U - U @ H_EFF)          # [H_EFF, U_g]
        obs = np.linalg.norm(T(dt) @ U - U @ T(dt))          # [T_dt, R_g]
        if gen > 1e-9:
            gen_nonzero += 1
        if obs > 1e-9:
            obstruction_nonzero += 1
    print(f"  generator [H_EFF,U_g] ≠ 0 for {gen_nonzero} of {len(G)} elements")
    print(f"  obstruction [T_t,R_g] ≠ 0 for {obstruction_nonzero} of {len(G)}")
    print(f"  ⇒ the two counts agree: the finite-time obstruction is generated")
    print(f"    by the infinitesimal one. Individuality is a cocycle whose")
    print(f"    source is exactly the seven distinct voice-energies of H_EFF.")
    ok = (functor_err < 1e-9 and gen_nonzero == 167
          and obstruction_nonzero == 167)
    print(f"  (167 of 168 = all but the identity.) {ok} [Т]")
    return ok


